     */
    public function store(Request $request)
    {
            $chat= new Chat();
            $chat->idUser = Auth::user()->id;
            $chat->userName = Auth::user()->name;
            $chat->idFriend = (1);
            $chat->mensaje = $request->input('mensaje');
            $chat->save();
            return redirect('inicio/chat');
    }

